    /// Push button.
    /// </summary>
    QT_PUSH_BUTTON = 0,

    /// <summary>
    /// Checkbox button.
    /// </summary>
    QT_CHECKBOX = 1,

    /// <summary>
    /// Radiobox button.
    /// </summary>
    QT_RADIOBOX = 2,

    /// <summary>
    /// Button should create a new buttonbar.
    /// </summary>
    QT_NEW_BUTTONBAR = 1024
}